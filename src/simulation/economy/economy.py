        audit_score= float(audit_score)

        fitness_evader = float(tax_expense) * (1 - audit_score) + (audit_score)*(-profit)

        fitness_auditor = -float(tax_expense) * (1 - audit_score)+ (audit_score)*(float(tax_expense))

        return (fitness_evader , fitness_auditor)
    
    
    

    
